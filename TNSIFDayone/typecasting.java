package dayone;

public class typecasting {

	public static void main(String[] args) {
		//widening is implicit type casting 
byte b=10;
int num1=b;
System.out.println(num1);

float f=22.14f;
double num2=f;
System.out.println(num2);
int a=6728;
short num3=(short)a;
System.out.println(num3);
// narrowing

double f1=10.52f;
long num4=(long)f1;
System.out.println(num4);

 long p1=93226059;
int num7=(int)p1;
System.out.println(num7);
	}

}
